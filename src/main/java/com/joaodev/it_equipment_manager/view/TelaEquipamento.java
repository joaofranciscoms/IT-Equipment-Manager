    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        try {
            Equipamento novoObj = new Equipamento();

            novoObj.setModelo(fldModelo.getText());
            novoObj.setFabricante(fldFabricante.getText());
            novoObj.setTipoEquipamento(TipoEquipamento.valueOf(fldTipoEquipamento.getText().toUpperCase()));

            Equipamento salvo = equipamentoService.salvar(novoObj);

            javax.swing.JOptionPane.showMessageDialog(this, "Equipamento " + salvo.getModelo() + " cadastrado com sucesso!");

            fldModelo.setText("");
            fldFabricante.setText("");
            fldTipoEquipamento.setText("");
        }
        catch(IllegalArgumentException exc){
            javax.swing.JOptionPane.showMessageDialog(this, "Tipo de equipamento inválido!");
        }
        catch(NoSuchElementException exc){
            javax.swing.JOptionPane.showMessageDialog(this, "Registro não encontrado!");
        }
        catch (DataIntegrityViolationException e){
            javax.swing.JOptionPane.showMessageDialog(this, "Já existe um equipamento com esses dados.");
        }
        catch (RuntimeException e){
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao acessar o banco de dados.");
        }
    }//GEN-LAST:event_btSalvarActionPerformed

    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        try {
            UUID id = UUID.fromString(fldInserirId.getText());

            equipamentoService.remover(id);

            javax.swing.JOptionPane.showMessageDialog(this, "Equipamento removido com sucesso!");
        }
        catch (IllegalArgumentException exc){
            javax.swing.JOptionPane.showMessageDialog(this, "Tipo de equipamento inválido!");
        }
        catch(RuntimeException exc){
            javax.swing.JOptionPane.showMessageDialog(this, exc.getMessage());
        }
    }//GEN-LAST:event_btRemoverActionPerformed

