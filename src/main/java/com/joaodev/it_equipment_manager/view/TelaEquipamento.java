    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        try{
            UUID id = UUID.fromString(fldInserirId.getText());
            
            Optional<Equipamento> retorno = equipamentoService.buscarPorId(id);
            
            if(retorno.isPresent()){
                Equipamento eq = retorno.get();
                
                fldResultadoModelo.setText(eq.getModelo());
                fldResultadoFabricante.setText(eq.getFabricante());
                fldResultadoTipo.setText(eq.getTipoEquipamento().name());
            }
            else{
                javax.swing.JOptionPane.showMessageDialog(this, "Esse ID não existe no Banco de Dados!");
            }
        }
        catch(IllegalArgumentException exc){
            javax.swing.JOptionPane.showMessageDialog(this, "ERRO - UUID Invállido!");
        }
        catch(RuntimeException exc){
            javax.swing.JOptionPane.showMessageDialog(this, exc.getMessage());
        }
    }//GEN-LAST:event_btBuscarActionPerformed

